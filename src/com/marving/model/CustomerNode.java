package com.marving.model;

public class CustomerNode {
	
	public int name;
	
	public int node;
	
	public int neededBand;
	public int totalNeededBand;
		
	public CustomerNode(int name, int node,int totalNeededBand){
		
		this.name = name;
		this.node = node;
		this.neededBand = totalNeededBand;
		this.totalNeededBand = totalNeededBand;
	}	
}
